# Concatenation (+)
first = "Python"
second = "is making waves"
print(first + " " + second) # Python is making waves

# Repetition (*)
print("Hi! " * 3)

# Old formatting (%)
name = "Alice"
age = 25
print("Name: %s, Age: %d" %(name, age))

# str.format() method
print("Name: {}, Age: {}".format(name, age))

# f-strings (Python 3.6+)
# Preferred modern method.
print(f"My name is {name} and I am {age} years old")
